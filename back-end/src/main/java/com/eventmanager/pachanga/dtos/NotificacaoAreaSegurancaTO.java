public class NotificacaoAreaSegurancaTO {
	
	private int codFesta;
	
	private String nomeFesta;
	
	private int codProblema;
	
	private String descProblema;
	
	private String descProblemaRelatado;
	
	private int codArea;
	
	private String nomeArea;
	
	private int codAreaProblema;

	public int getCodFesta() {
		return codFesta;
	}

	public void setCodFesta(int codFesta) {
		this.codFesta = codFesta;
	}

	public String getNomeFesta() {
		return nomeFesta;
	}

	public void setNomeFesta(String nomeFesta) {
		this.nomeFesta = nomeFesta;
	}

	public int getCodProblema() {
		return codProblema;
	}

	public void setCodProblema(int codProblema) {
		this.codProblema = codProblema;
	}

	public String getDescProblema() {
		return descProblema;
	}

	public void setDescProblema(String descProblema) {
		this.descProblema = descProblema;
	}

	public int getCodArea() {
		return codArea;
	}

	public void setCodArea(int codArea) {
		this.codArea = codArea;
	}

	public String getNomeArea() {
		return nomeArea;
	}

	public void setNomeArea(String nomeArea) {
		this.nomeArea = nomeArea;
	}

	public String getDescProblemaRelatado() {
		return descProblemaRelatado;
	}

	public void setDescProblemaRelatado(String descProblemaRelatado) {
		this.descProblemaRelatado = descProblemaRelatado;
	}

	public int getCodAreaProblema() {
		return codAreaProblema;
	}

	public void setCodAreaProblema(int codAreaProblema) {
		this.codAreaProblema = codAreaProblema;
	}

}
